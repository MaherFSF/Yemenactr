export type IndicatorFamily = 
  | 'macro_real_economy'
  | 'prices_cost_of_living'
  | 'monetary_financial'
  | 'public_finance'
  | 'trade_external'
  | 'humanitarian_social'
  | 'aid_projects'
  | 'conflict_political'
  | 'private_sector'
  | 'sanctions_restrictions';

export type ConfidenceRating = 'A' | 'B' | 'C' | 'D';
export type RegimeTag = 'aden_irg' | 'sanaa_defacto' | 'mixed' | 'unknown';
export type Frequency = 'daily' | 'weekly' | 'monthly' | 'quarterly' | 'annual';
export type GeoLevel = 'national' | 'governorate' | 'district' | 'city';

export interface IndicatorDefinition {
  code: string;
  nameEn: string;
  nameAr: string;
  descriptionEn: string;
  descriptionAr: string;
  family: IndicatorFamily;
  unit: string;
  frequency: Frequency;
  geoLevel: GeoLevel;
  splitSystem: boolean; // Whether indicator differs by regime
  baseYear?: number;
  methodology?: string;
  sources: string[];
  startYear: number;
  confidenceDefault: ConfidenceRating;
}

