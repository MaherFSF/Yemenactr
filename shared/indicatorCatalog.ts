export function getIndicatorByCode(code: string): IndicatorDefinition | undefined {
  return allIndicators.find(i => i.code === code);
}

export function getIndicatorsByFamily(family: IndicatorFamily): IndicatorDefinition[] {
  return allIndicators.filter(i => i.family === family);
}

export function getIndicatorsBySplitSystem(splitSystem: boolean): IndicatorDefinition[] {
  return allIndicators.filter(i => i.splitSystem === splitSystem);
}

export function getIndicatorsByFrequency(frequency: Frequency): IndicatorDefinition[] {
  return allIndicators.filter(i => i.frequency === frequency);
}

export function getIndicatorsByGeoLevel(geoLevel: GeoLevel): IndicatorDefinition[] {
  return allIndicators.filter(i => i.geoLevel === geoLevel);
}

export function searchIndicators(query: string): IndicatorDefinition[] {
  const lowerQuery = query.toLowerCase();
  return allIndicators.filter(i => 
    i.code.toLowerCase().includes(lowerQuery) ||
    i.nameEn.toLowerCase().includes(lowerQuery) ||
    i.nameAr.includes(query) ||
    i.descriptionEn.toLowerCase().includes(lowerQuery) ||
    i.descriptionAr.includes(query)
  );
}

