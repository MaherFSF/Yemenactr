// Helper functions
export const getDocumentsByYear = (year: number) => 
  researchDocuments.filter(doc => doc.date.startsWith(year.toString()));

export const getDocumentsBySource = (sourceName: string) =>
  researchDocuments.filter(doc => doc.source.nameEn === sourceName);

export const getDocumentsByCategory = (category: string) =>
  researchDocuments.filter(doc => doc.category === category);

export const getFeaturedDocuments = () =>
  researchDocuments.filter(doc => doc.featured);

export const getDocumentsBySector = (sector: string) =>
  researchDocuments.filter(doc => doc.sectors.includes(sector));

// Get unique values for filters
export const getUniqueCategories = () => 
  Array.from(new Set(researchDocuments.map(doc => doc.category))).sort();

export const getUniqueSources = () =>
  Array.from(new Set(researchDocuments.map(doc => doc.source.nameEn))).sort();

export const getUniqueYears = () =>
  Array.from(new Set(researchDocuments.map(doc => doc.date.substring(0, 4)))).sort().reverse();

export const getUniqueTypes = () =>
  Array.from(new Set(researchDocuments.map(doc => doc.type)));
